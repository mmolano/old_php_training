            <span><p class="textcenter">
                   James Gunn déclare : « Groot est le seul véritable ami de Rocket. Il n’a que trois mots pour communiquer, mais son histoire est à vous briser le cœur »

                    Groot est une créature végétale humanoïde très avancée.

                    Kevin Feige ajoute : « Nous avons pris beaucoup de plaisir à travailler avec Vin Diesel pour faire la voix de Groot. C’est un homme plein d’énergie et de passion. Quand je l’ai appelé pour lui demander si cela l’intéresserait de jouer un personnage qui est, pour l’essentiel, un arbre et ne prononce que trois mots, il m’a dit sans hésiter : « Compte sur moi ».

                    Vin Diesel explique : « C’est l’unique phrase de dialogue que prononce Groot qui m’a convaincu et m’a fait comprendre le personnage. La façon dont ils ont réussi à renforcer le thème, le sens et le message de tout le film en une seule ligne de dialogue m’a bluffé. Et la façon dont cette phrase se traduit dans la troisième partie du film va vraiment faire réagir le public et lui donner l’impression de faire intégralement partie de ce monde et d’en comprendre les enjeux ; il va ressentir ce que traversent les personnages. »

