            <span><p class="textcenter">
                    Peter Quill (Chris Pratt) 
                   Après une longue recherche, c’est à Chris Pratt que la production et le réalisateur ont proposé le rôle de Peter Quill, un jeune aventurier de l’espace aussi audacieux qu’effronté. Kevin Feige déclare : « Nous avons auditionné de nombreux acteurs et effectué des bouts d’essais poussés avec beaucoup d’entre eux, et Chris Pratt l’a emporté de loin. Il était cool, sexy, extrêmement drôle par certains aspects –c’était juste ce qu’il nous fallait. Quand il enfile le costume et le masque et qu’il brandit ses pistolets de l’espace, il est juste… parfait ! On y croit ! »

                    L’histoire est racontée du point de vue de Quill, et Chris Pratt confie : « Je me suis immédiatement senti proche du personnage par ses aspects les plus juvéniles. Au fond, c’est encore un gamin. Il n’a jamais connu son père, il a été aspiré dans l’espace au moment où sa mère mourait et a été élevé par un humanoïde à peau bleue. Toute sa vie, on lui a répété qu’il fallait s’endurcir, et le résultat, c’est qu’il a acquis un côté bravache et provocateur. Au fond, c’est un garçon solitaire, et même s’il voyage dans l’espace et qu’il fait ce qu’il veut, sa famille, sa communauté lui manquent. Au fil de l’histoire, il va apprendre à faire à nouveau attention aux autres et évoluer. »

