                   Zoe Saldana interprète Gamora, une meurtrière à la peau verte. James Gunn explique : « Zoe a été choisie très en amont dans le développement du film, et elle a fortement influencé son personnage en travaillant avec moi au fil des réécritures. Elle a beaucoup apporté à Gamora, et nous avons créé ensemble un personnage féminin qui en impose mais qui a aussi ses défauts. Zoe est impressionnante dans le rôle, elle n’a pas eu peur de jouer son personnage sans chercher à se faire aimer du public à chaque seconde.

