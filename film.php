                   James Gunn déclare : « Rocket est loin de nager dans le bonheur. C’est une créature triste et infirme qui a été « démontée », puis « remontée » ensuite. Il a connu de grandes souffrances dans sa vie, ce qui en a fait un être pragmatique et qui, plus que n’importe qui d’autre, a les pieds sur terre. C’est probablement lui le cœur du film, à tous les sens du terme »

